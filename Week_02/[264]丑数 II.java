class Solution {
    private int[] uglyNumber = {2,3,5};
    public int nthUglyNumber(int n) {
        //åˆ›å»ºå°æ ¹å †ï¼Œæ¯æ¬¡å‡ºå †çš„éƒ½æ˜¯æœ€å°å€¼
        Queue<Long> queue = new PriorityQueue<>();
        queue.add(1L);
        //è®°å½•å‡ºå †çš„ä¸ªæ•°ï¼Œå‡ºå †çš„å…ƒç´ å®Œå…¨æŒ‰ç…§ä»å°åˆ°å¤§æ’åº
        int count = 0;
        while (! queue.isEmpty()){
            long cut = queue.poll();
            //å¦‚æœå‡ºå †çš„ä¸ªæ•°>=n,å½“å‰cutå°±æ˜¯ç¬¬nä¸ªä¸‘æ•°
            if(++count >= n){
                return (int) cut;
            }
            //éä¹˜ 2,3,5
            for(int num : uglyNumber){
                //æ’é™¤é‡å¤çš„æ•°å­—
                if(! queue.contains(num * cut)){
                    queue.add(num * cut);
                }
            }
        }
        return -1;
    }
}

